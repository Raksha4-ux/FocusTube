function getCategoryIcon(category) {
  return CATEGORY_ICONS[category] || CATEGORY_ICONS['other'];
}


function getYearProgress() {
  const now = new Date();
  const year = now.getFullYear();

  const startOfYear = new Date(year, 0, 1);
  const startOfNextYear = new Date(year + 1, 0, 1);

  const elapsedMs = now.getTime() - startOfYear.getTime();
  const totalMs = startOfNextYear.getTime() - startOfYear.getTime();

  if (elapsedMs <= 0) return 0;

  const progress = (elapsedMs / totalMs) * 100;
  return Math.min(100, Math.max(0, progress));
}

function renderYearProgress() {
  const container = document.getElementById("year-progress");
  if (!container) return;

  const percentage = getYearProgress();
  const gone = percentage.toFixed(1);
  const remaining = (100 - percentage).toFixed(1);

  container.innerHTML = `
    <div>${gone}% of the year is gone</div>
    <div>${remaining}% still remains</div>
    <div style="margin-top: 6px;">Did you move closer to your goals today?</div>
  `;
}


function formatDuration(seconds) {
  const totalMinutes = Math.floor(seconds / 60);
  const hours = Math.floor(totalMinutes / 60);
  const minutes = totalMinutes % 60;

  if (hours > 0) {
    return `${hours}h ${minutes}m`;
  }
  return `${minutes}m`;
}


function renderAnalyticsDashboard() {
  const container = document.getElementById("analytics-list");
  if (!container) return;

  chrome.storage.local.get(ANALYTICS_STORAGE_KEY, (data) => {
    const analytics = data[ANALYTICS_STORAGE_KEY] || {};
    const now = new Date();
    const cutoff = new Date(
      now.getFullYear(),
      now.getMonth(),
      now.getDate() - 29
    ); // last 30 days including today

    // Sum seconds per category over the last 30 days
    const totals = {};

    for (const [dateStr, categories] of Object.entries(analytics)) {
      const date = new Date(dateStr);
      if (isNaN(date.getTime())) continue;
      if (date < cutoff) continue;

      for (const [category, seconds] of Object.entries(categories)) {
        if (typeof seconds !== "number") continue;
        totals[category] = (totals[category] || 0) + seconds;
      }
    }

    // Clear old content
    container.innerHTML = "";

    const entries = ANALYTICS_CATEGORIES_ORDER.filter(
      (cat) => totals[cat] && totals[cat] > 0
    ).map((cat) => [cat, totals[cat]]);

    if (entries.length === 0) {
      const empty = document.createElement("div");
      empty.className = "analytics-empty";
      empty.textContent =
        "No analytics yet. Watch some focused content and come back.";
      container.appendChild(empty);
      return;
    }

    for (const [category, seconds] of entries) {
      const row = document.createElement("div");
      row.className = "analytics-row";

      // Create icon element with inline SVG
      const iconSpan = document.createElement("span");
      iconSpan.className = "category-icon";
      iconSpan.innerHTML = getCategoryIcon(category);

      const label = document.createElement("span");
      label.className = "analytics-category";
      label.textContent =
        category.charAt(0).toUpperCase() + category.slice(1);

      const time = document.createElement("span");
      time.className = "analytics-time";
      time.textContent = formatDuration(seconds);

      row.appendChild(iconSpan);
      row.appendChild(label);
      row.appendChild(time);
      container.appendChild(row);
    }
  });
}

// Set up UI event handlers
function bindEvents() {
  const openBtn = document.getElementById("open-youtube");
  if (openBtn) {
    openBtn.addEventListener("click", () => {
      chrome.tabs.create({ url: "https://www.youtube.com/" });
    });
  }

  const analyticsBtn = document.getElementById("open-analytics");
  if (analyticsBtn) {
    analyticsBtn.addEventListener("click", () => {
      chrome.tabs.create({ url: chrome.runtime.getURL("analytics.html") });
    });
  }
}

// Initialize popup logic when DOM is ready
document.addEventListener("DOMContentLoaded", () => {
  renderYearProgress();
  renderAnalyticsDashboard();
  bindEvents();
});

